public class Star_02 {

	public static void main(String[] args) {
		
		for(int i=1;i<=4;i++) {//outer for loop
			for(int j=1;j<=4;j++) {//inner for loop
				System.out.print("* ");
			}
			System.out.println();
		}
	}
}
